# None = False
something = None
if something:
    print("This is true")
else:
    print("This is false")

# Empty string = False
something = ""
if something:
    print("This is true")
else:
    print("This is false")

# String with something = True
something = "something"
if something:
    print("This is true")
else:
    print("This is false")
